function table_fill(bodys,handles)
    Data = [zeros(1,12);zeros(1,12);zeros(1,12)];
    for i = (1: length(bodys))
        Data(i,:) = [bodys(i).x0,bodys(i).y0,bodys(i).v0,bodys(i).alpha,bodys(i).x_t,bodys(i).y_t,bodys(i).t_t,bodys(i).x1,bodys(i).y1,bodys(i).ymax,bodys(i).S,bodys(i).t];
    end
    set(handles.uitable2,'Data',Data)

function new_bodys = move_in_target_econom(bodys)
    G = 9.8;
    for i = (1: length(bodys))
        bodys(i).a = atan((bodys(i).y_t+0.5*G*1^2)/bodys(i).x_t);
        bodys(i).alpha = bodys(i).a*180/pi;
        bodys(i).v0 = bodys(i).x_t/cos(bodys(i).a)*1;
        bodys(i).t = (bodys(i).v0*sin(bodys(i).a)+(bodys(i).v0^2*sin(bodys(i).a)^2+2*G*bodys(i).y0)^0.5)/G;
        bodys(i).x1 = bodys(i).x0+bodys(i).v0*cos(bodys(i).a)*bodys(i).t;
        bodys(i).ymax = bodys(i).y0+bodys(i).v0*sin(bodys(i).a)*(bodys(i).t/2) - 0.5 * G * (bodys(i).t/2)^2;
        bodys(i).ts = [0:0.1:bodys(i).t,bodys(i).t];
        bodys(i).xs = bodys(i).x0+bodys(i).v0*cos(bodys(i).a)*bodys(i).ts;
        bodys(i).ys = bodys(i).y0+bodys(i).v0*sin(bodys(i).a)*bodys(i).ts - 0.5 * G * bodys(i).ts.^2;
        bodys(i).S = abs(bodys(i).x1-bodys(i).x0);
    end
    new_bodys = bodys;

function new_bodys = move_in_target(bodys)
    G = 9.8;
    for i = (1: length(bodys))
        bodys(i).a = atan((bodys(i).y_t+0.5*G*bodys(i).t_t^2)/bodys(i).x_t);
        bodys(i).alpha = bodys(i).a*180/pi;
        bodys(i).v0 = bodys(i).x_t/cos(bodys(i).a)*bodys(i).t_t;
        bodys(i).t = (bodys(i).v0*sin(bodys(i).a)+(bodys(i).v0^2*sin(bodys(i).a)^2+2*G*bodys(i).y0)^0.5)/G;
        bodys(i).x1 = bodys(i).x0+bodys(i).v0*cos(bodys(i).a)*bodys(i).t;
        bodys(i).ymax = bodys(i).y0+bodys(i).v0*sin(bodys(i).a)*(bodys(i).t/2) - 0.5 * G * (bodys(i).t/2)^2;
        bodys(i).ts = [0:0.1:bodys(i).t,bodys(i).t];
        bodys(i).xs = bodys(i).x0+bodys(i).v0*cos(bodys(i).a)*bodys(i).ts;
        bodys(i).ys = bodys(i).y0+bodys(i).v0*sin(bodys(i).a)*bodys(i).ts - 0.5 * G * bodys(i).ts.^2;
        bodys(i).S = abs(bodys(i).x1-bodys(i).x0);
    end
    new_bodys = bodys;

% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
    body = struct('x0',0,'y0',0,'alpha',0,'a',0,'v0',0,'x_t',0,'y_t',0,'t_t',0,'x1',0,'y1',0,'ymax',0,'S',0,'t',0,'ts',[],'xs',[],'ys',[]);
    bodys = [body; body; body];

    if get(handles.radiobutton2,'Value')
        bodys(1).x_t = str2double(get(handles.edit5,'String'));
        bodys(1).y_t = str2double(get(handles.edit6,'String'));
        bodys(1).t_t = str2double(get(handles.edit7,'String'));
        
        bodys(2).x_t = str2double(get(handles.edit8,'String'));
        bodys(2).y_t = str2double(get(handles.edit9,'String'));
        bodys(2).t_t = str2double(get(handles.edit10,'String'));
        
        bodys(3).x_t = str2double(get(handles.edit11,'String'));
        bodys(3).y_t = str2double(get(handles.edit12,'String'));
        bodys(3).t_t = str2double(get(handles.edit13,'String'));
        bodys = move_in_target(bodys);
        table_fill(bodys,handles)

        hold(handles.axes1,'off')
        cla(handles.axes2,'reset')
        plot(handles.axes1, bodys(1).xs,bodys(1).ys)
        
        grid(handles.axes1, 'on')
        hold(handles.axes1,'on')
        plot(handles.axes1, bodys(1).x_t,bodys(1).y_t,'o')
        
        plot(handles.axes1, bodys(2).xs,bodys(2).ys,'red')
        plot(handles.axes1, bodys(2).x_t,bodys(2).y_t,'or')
    
        plot(handles.axes1, bodys(3).xs,bodys(3).ys,'black')
        plot(handles.axes1, bodys(3).x_t,bodys(3).y_t,'oblack')
        
        title(handles.axes1,'x(y) and targets')
        legend(handles.axes1,{'y1(x1)','y_1t:x_1t','y2(x2)','y_2t:x_2t','y3(x3)','y_3t:x_3t'});
        grid(handles.axes1, 'on')
    end
    if get(handles.radiobutton6,'Value')
        bodys(1).x_t = str2double(get(handles.edit28,'String'));
        bodys(1).y_t = str2double(get(handles.edit29,'String'));
        
        bodys = move_in_target_econom(bodys);
        table_fill(bodys,handles)
        
        cla(handles.axes2,'reset')
        hold(handles.axes1,'off')
        plot(handles.axes1, bodys(1).xs,bodys(1).ys)
        grid(handles.axes1, 'on')
        hold(handles.axes1,'on')
        plot(handles.axes1, bodys(1).x_t,bodys(1).y_t,'o')
        title(handles.axes1,'y(x)')
        legend(handles.axes1,{'y(x)','y_t:x_t'});
        fprintf('\n')
        fprintf('%f ',bodys(1).xs)
        fprintf('\n')
        fprintf('%f ',bodys(1).ys)
    end


